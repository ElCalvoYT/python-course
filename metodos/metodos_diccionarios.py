diccionario = {
    'name': "Juansi",
    'apellido': "Ibarra Podhajecki",
    'edad': 14,
    'pais': "Argentina",
}

#----------------------------------------------------------------

# Get all keys available in the dictionary
#* returns a list of all keys

diccionario.keys()

#----------------------------------------------------------------

# Get the value of a key
diccionario.get("name")

#----------------------------------------------------------------

# Remove an element from the dictionary by its key
diccionario.pop("name")

#----------------------------------------------------------------

#Iterate through the dictionary
diccionario.items()

#----------------------------------------------------------------

# Remove all elements from the dictionary

diccionario.clear()

